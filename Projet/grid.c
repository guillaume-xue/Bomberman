#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include <unistd.h>

#define ROWS 32
#define COLS 16

int main() {
  srand(time(NULL));

  for (int k = 0; k < ROWS * COLS; k++) {
    system("clear");
    int rd = rand() % (ROWS * COLS / 2);

    for (int i = 0; i < ROWS; ++i) {
      if (i % 2 == 0) {
        for (int j = 0; j < COLS; ++j) {
          printf("+---");
        }
        printf("+\n");
      } else {
        for (int j = 0; j < COLS; ++j) {
          if (rd == (i / 2) * COLS + j) {
            printf("| ■ ");
          } else {
            printf("|   ");
          }
        }
        printf("|\n");
      }
    }

    for (int j = 0; j < COLS; ++j) {
      printf("+---");
    }
    printf("+\n");

    sleep(1);
  }

  return 0;
}

